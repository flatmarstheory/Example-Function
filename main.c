#include <stdio.h>
// ਇਹ ਫੰਕਸ਼ਨ ਦੋ ਪੂਰਨ ਸੰਖਿਆਵਾਂ ਨੂੰ ਜੋੜਦਾ ਹੈ ਅਤੇ ਉਨ੍ਹਾਂ ਦਾ ਜੋੜ ਵਾਪਸ ਕਰਦਾ ਹੈ
int add(int a, int b) { return a + b; }
/*
ਮੁੱਖ ਫੰਕਸ਼ਨ add() ਫੰਕਸ਼ਨ ਨੂੰ 5 ਅਤੇ 3 ਨਾਲ ਕਾਲ ਕਰਦਾ ਹੈ,
ਨਤੀਜੇ ਨੂੰ ਸਟੋਰ ਕਰਦਾ ਹੈ, ਅਤੇ ਫਿਰ ਇਸਨੂੰ ਪ੍ਰਿੰਟ ਕਰਦਾ ਹੈ।
*/
int main() {
  int result = add(5, 3); // add() ਫੰਕਸ਼ਨ ਨੂੰ ਕਾਲ ਕਰੋ ਅਤੇ ਨਤੀਜਾ ਸਟੋਰ ਕਰੋ
  printf("Result: %d\n", result); // ਨਤੀਜਾ ਪ੍ਰਿੰਟ ਕਰੋ
  return 0;
}